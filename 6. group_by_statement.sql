-- 6. What is the total cost of repairs per department?
SELECT
    Sales_Person_Department,
    SUM(Cost_of_Repairs) AS Total_Repair_Cost
FROM [pc_database].[dbo].[pc_data_v2]
GROUP BY Sales_Person_Department;

-- 7. How many orders fall into each priority level?
SELECT
    Priority,
    COUNT(*) AS Total_Orders
FROM [pc_database].[dbo].[pc_data_v2]
GROUP BY Priority;

-- 8. What is the total sale price generated by each shop?
SELECT
    Shop_Name,
    SUM(Sale_Price) AS Total_Sale_Price
FROM [pc_database].[dbo].[pc_data_v2]
GROUP BY Shop_Name;

-- 9. How many PCs were sold for each storage type (SSD vs HDD)?
SELECT
    Storage_Type,
    COUNT(*) AS PCs_Sold
FROM [pc_database].[dbo].[pc_data_v2]
GROUP BY Storage_Type;

-- 10. What is the total finance amount taken up per country/state?
SELECT
    Country_or_State,
    SUM(Finance_Amount) AS Total_Finance_Amount
FROM [pc_database].[dbo].[pc_data_v2]
GROUP BY Country_or_State;