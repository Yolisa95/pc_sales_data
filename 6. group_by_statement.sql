---------------------------------------------------------------------------------
-- AGGREGATIONS AND GROUP BY STATEMENT
---------------------------------------------------------------------------------

-- 1. How many sales did we make in each continent?
SELECT
    Continent,
    COUNT(*) AS Total_Sales
FROM [pc_database].[dbo].[pc_data_v2]
GROUP BY Continent;

-- 2. What is the total sales value for each payment method?
SELECT
    Payment_Method,
    SUM(Sale_Price) AS Total_Sales_Value
FROM [pc_database].[dbo].[pc_data_v2]
GROUP BY Payment_Method;

-- 3. How many PCs did we sell per brand (make)?
SELECT
    PC_Make,
    COUNT(*) AS PCs_Sold
FROM [pc_database].[dbo].[pc_data_v2]
GROUP BY PC_Make;

-- 4. What is the total discount we gave out per sales person?
SELECT
    Sales_Person_Name,
    SUM(Discount_Amount) AS Total_Discount
FROM [pc_database].[dbo].[pc_data_v2]
GROUP BY Sales_Person_Name;

-- 5. How many sales came through each channel (Online vs Offline)?
SELECT
    Channel,
    COUNT(*) AS Total_Sales
FROM [pc_database].[dbo].[pc_data_v2]
GROUP BY Channel;

